package cm.ubuea.covider.profile_module.dto;

public class NewLocation {
    String name;
    String id;
    String town;

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

	public String getTown() {
		return town;
	}

	public void setTown(String town) {
		this.town = town;
	}
    
    
}
